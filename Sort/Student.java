public class Student {

	private int number;
	private int password;

	public Student(int number,int password) {
		this.number = number;
		this.password = password;
	}

	/**
	 * @return the number
	 */
	public int getNumber() {
		return number;
	}

	/**
	 * @return the password
	 */
	public int getPassword() {
		return password;
	}	
}
